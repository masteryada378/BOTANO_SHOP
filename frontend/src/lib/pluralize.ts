/**
 * Утиліта для правильної відміни числівників в українській мові.
 *
 * Правило відміни:
 * - закінчення 1 (але не 11) → форма "один" (товар)
 * - закінчення 2-4 (але не 12-14) → форма "кілька" (товари)
 * - решта → форма "багато" (товарів)
 *
 * Приклади:
 *   pluralizeUk(1, "товар", "товари", "товарів") → "1 товар"
 *   pluralizeUk(3, "товар", "товари", "товарів") → "3 товари"
 *   pluralizeUk(11, "товар", "товари", "товарів") → "11 товарів"
 *   pluralizeUk(21, "товар", "товари", "товарів") → "21 товар"
 */
export const pluralizeUk = (
    count: number,
    one: string,
    few: string,
    many: string,
): string => {
    const abs = Math.abs(count);
    const mod10 = abs % 10;
    const mod100 = abs % 100;

    // Числа 11-14 завжди беруть форму "багато" (виняток з загального правила)
    if (mod100 >= 11 && mod100 <= 14) return `${count} ${many}`;

    if (mod10 === 1) return `${count} ${one}`;
    if (mod10 >= 2 && mod10 <= 4) return `${count} ${few}`;
    return `${count} ${many}`;
};
